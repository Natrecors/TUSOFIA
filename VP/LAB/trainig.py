my_dict = {}
print("Моля, въведете 5 числа:")

for i in range(5):
    user_input = int(input(f"Въведете число {i+1}: "))
    my_list.append(user_input)

for number in my_list:
    if number % 2 == 0:
        my_dict[number] = "Четно"
    else:
        my_dict[number] = "Нечетно"


